// Switch case

let permissao = 'gerente'

switch (permissao) {
     case 'comum':
          console.log('Usuario comum')
     break;

     case 'gerente' :
          console.log('Usuario gerente')
     break

     case 'diretor':
          console.log('usuario diretos')
     break

     default:
          console.log('usuario nao reconhecido')
}